                            Dinas Pendidikan Pemuda dan Olah Raga Kebupaten Kebumen dibentuk berdasarkan Peraturan Daerah Kabupaten Kebumen Nomor 13 Tahun 2010, tentang Pembentukan Organisasi dan Tata Kerja Dinas Pendidikan Pemuda dan Olah Raga Kabupaten Kebumen. Sedangkan Tugas Pokok Dinas Pendidikan Pemuda dan Olah Raga Kabupaten Kebumen sesuai Pasal 2 Peraturan Bupati Kebumen Nomor 68 Tahun 2010 adalah melaksanakan urusan pemerintah daerah di bidang pendidikan, pemuda dan olah raga berdasarkan asas otonomi dan tugas pembantuan. Untuk melaksanakan tugas sebagaimana dimaksud Pasal 2 Peraturan Bupati Kebumen Nomor 68 Tahun 2010 Dinas Pendidikan Pemuda dan Olah Raga Kabupaten Kebumen mempunyai fungsi; Pelaksanaan perumusan kebijakan teknis, pemberian bimbingan dan pembinaan dalam bidang pendidikan, pemuda dan olah raga; Pelaksanaan perancangan teknis operasional dan pengembangan unsur-unsur pendidikan yang menjadi tanggung jawabnya, Pelaksanaan pemberian perizinan berdasarkan perundang-undangan yang berlaku, Pelaksanaan pengawasan dan pengendalian teknis atas pelaksanaan tugas pokok dan fungsinya; Pelaksanaan pengurusan administrasi Dinas Pendidikan, Pemuda dan Olah Raga dan Pelaksanaan tugas lain yang diberikan oleh Bupati sesuai dengan tugas dan fungsinya. Sesuai Peraturan Daerah Kabupaten Kebumen Nomor 7 Tahun 2016 Tentang Pembentukan Dan Susunan Perangkat Daerah Kabupaten Kebumen, dan perubahan struktur organisasi dan tata kerja yang diatur dalam Perbup Kabupaten Kebumen Nomor 66 Tahun 2016 tentang Kedudukan, Susunan Organisasi, Tugas Dan Fungsi, Serta Tata Kerja Dinas Pendidikan Kabupaten Kebumen. Pada Tanggal 1 Januari 2017 Dinas Pendidikan Pemuda dan Olahraga berganti nama menjadi Dinas Pendidikan Kabupaten Kebumen.                        </p>

